package model;
public class Pessoas {

    public String nome;
    public int idade;
    public int Id;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }
    public int getId() {
        return Id;
    }
    public void setId(int id) {
        Id = id;
    }
}
